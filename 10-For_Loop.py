'''
    1- In Python for-loop is not same as other language like c++,java,javascript etc.
    2- For-loop using we access number and string
    3 -In for-loop use range() function 
'''

# for-loop
# Simple to read number value
age=(20,21,24,25,18,26)
for num in age:
    print(num)


# Read String
name="narsi"
for c in name:
    print(c)


# we can use range() function
# range() take three parameter
# range(start,stop,step)
for v in range(2,21,2):
    print(v)



