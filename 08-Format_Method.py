# Using the format method.
# 1- format can be assign with index.
# 2- and format can be assign with variable.
print("1- There is only {0} man and work on {1}".format(1,"Python"))
print("2- I work on {x} {var}".format(x="varsion",var=3.6))
print('{:<20}'.format("text"))
print('{:>20}'.format("text"))
print(" This is binary number of 21 = {:b}".format(21))
print(" This is hexa number of 21 = {:x}".format(21))
print(" This is ocatal number of 21 = {:o}".format(21))

